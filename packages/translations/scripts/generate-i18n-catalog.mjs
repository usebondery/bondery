/**
 * Generates manifest-aligned Catalog types and per-namespace branch prefix unions.
 * Run before generate-client-i18n-hooks.mjs.
 */
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = dirname(fileURLToPath(import.meta.url));
const localesEnRoot = join(packageRoot, "../src/locales/en");
const manifest = JSON.parse(readFileSync(join(packageRoot, "../manifest.json"), "utf8"));
const outDir = join(packageRoot, "../src/generated/i18n");

/** @type {Record<string, string[]>} */
export const branchPrefixesByNamespace = {};

function catalogTypeName(namespace) {
  if (namespace === "common") {
    return "CommonCatalog";
  }
  if (namespace === "validation") {
    return "ValidationCatalog";
  }
  if (namespace === "glossary") {
    return "GlossaryCatalog";
  }
  return `${namespace}Catalog`;
}

export function prefixTypeName(namespace) {
  if (namespace === "common") {
    return "CommonPrefix";
  }
  if (namespace === "validation") {
    return "ValidationPrefix";
  }
  if (namespace === "glossary") {
    return "GlossaryPrefix";
  }
  return `${namespace}Prefix`;
}

function tsPropertyKey(key) {
  return /^[A-Za-z_][A-Za-z0-9_]*$/.test(key) ? key : JSON.stringify(key);
}

function jsonValueToTsType(value, indent) {
  if (typeof value === "string") {
    return "string";
  }
  if (value && typeof value === "object" && !Array.isArray(value)) {
    const pad = "  ".repeat(indent);
    const inner = Object.entries(value)
      .map(
        ([key, nested]) =>
          `  ${pad}${tsPropertyKey(key)}: ${jsonValueToTsType(nested, indent + 1)};`,
      )
      .join("\n");
    return `{\n${inner}\n${pad}}`;
  }
  return "string";
}

function collectBranchPrefixes(value, prefix = "", acc = []) {
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    return acc;
  }
  for (const [key, nested] of Object.entries(value)) {
    if (nested && typeof nested === "object" && !Array.isArray(nested)) {
      const path = prefix ? `${prefix}.${key}` : key;
      acc.push(path);
      collectBranchPrefixes(nested, path, acc);
    }
  }
  return acc;
}

export function generateI18nCatalog() {
  mkdirSync(outDir, { recursive: true });

  const catalogTypeLines = [];
  const prefixTypeLines = [];
  const namespacePrefixMapLines = [];

  for (const [namespace, entry] of Object.entries(manifest.namespaces).sort(([a], [b]) =>
    a.localeCompare(b),
  )) {
    const jsonPath = join(localesEnRoot, entry.path);
    const json = JSON.parse(readFileSync(jsonPath, "utf8"));
    const typeName = catalogTypeName(namespace);
    const prefixName = prefixTypeName(namespace);
    const prefixes = collectBranchPrefixes(json);

    branchPrefixesByNamespace[namespace] = prefixes;

    catalogTypeLines.push(`export type ${typeName} = ${jsonValueToTsType(json, 0)};`);
    catalogTypeLines.push("");

    if (prefixes.length === 0) {
      prefixTypeLines.push(`export type ${prefixName} = never;`);
    } else {
      prefixTypeLines.push(
        `export type ${prefixName} = ${prefixes.map((p) => JSON.stringify(p)).join(" | ")};`,
      );
    }
    prefixTypeLines.push("");

    namespacePrefixMapLines.push(`  ${JSON.stringify(namespace)}: ${prefixName};`);
  }

  const catalogEntries = Object.keys(manifest.namespaces)
    .sort()
    .map((namespace) => `  ${JSON.stringify(namespace)}: ${catalogTypeName(namespace)};`)
    .join("\n");

  const catalogTypesTs = [
    "/** Generated by packages/translations/scripts/generate-i18n-catalog.mjs — do not edit. */",
    "",
    ...catalogTypeLines,
    "export interface Catalog {",
    catalogEntries,
    "}",
    "",
  ].join("\n");

  const catalogPrefixesTs = [
    "/** Generated by packages/translations/scripts/generate-i18n-catalog.mjs — do not edit. */",
    "",
    ...prefixTypeLines,
    "export interface NamespacePrefixes {",
    namespacePrefixMapLines.join("\n"),
    "}",
    "",
  ].join("\n");

  writeFileSync(join(outDir, "catalog-types.ts"), catalogTypesTs);
  writeFileSync(join(outDir, "catalog-prefixes.ts"), catalogPrefixesTs);
  patchI18nextCatalogTypes();

  console.log("generate-i18n-catalog: ok");
}

/** Align i18next-cli module augmentation with manifest Catalog keys (not file paths). */
export function patchI18nextCatalogTypes() {
  const i18nextDts = join(packageRoot, "../src/generated/i18next-cli/i18next.d.ts");
  const content = `// Patched by packages/translations/scripts/generate-i18n-catalog.mjs — do not edit.
import type { Catalog } from "../i18n/catalog-types";

declare module "i18next" {
  interface CustomTypeOptions {
    defaultNS: "common";
    enableSelector: false;
    resources: Catalog;
  }
}
`;
  writeFileSync(i18nextDts, content);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  generateI18nCatalog();
}
