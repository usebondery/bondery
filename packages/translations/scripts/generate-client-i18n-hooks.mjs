/**
 * Generates per-platform namespace hook modules for webapp, mobile, and chrome-extension.
 * Run after i18next-cli types: node scripts/generate-client-i18n-hooks.mjs
 */
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import {
  branchPrefixesByNamespace,
  generateI18nCatalog,
  prefixTypeName,
} from "./generate-i18n-catalog.mjs";

const packageRoot = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(packageRoot, "../../..");
const manifest = JSON.parse(readFileSync(join(packageRoot, "../manifest.json"), "utf8"));

generateI18nCatalog();

const PLATFORMS = ["web", "mobile", "extension"];

function namespacesForPlatform(platform) {
  return Object.entries(manifest.namespaces)
    .filter(([, entry]) => entry.platforms.includes(platform))
    .map(([name]) => name)
    .sort();
}

function hookBaseName(namespace) {
  if (namespace === "common") {
    return "Common";
  }
  if (namespace === "validation") {
    return "Validation";
  }
  if (namespace === "glossary") {
    return "Glossary";
  }
  return namespace;
}

function useHookName(namespace) {
  return `use${hookBaseName(namespace)}Translations`;
}

function getHookName(namespace) {
  return `get${hookBaseName(namespace)}Translations`;
}

function prefixImportsForNamespaces(namespaces) {
  const types = namespaces.map((ns) => prefixTypeName(ns)).sort();
  return `import type { ${types.join(", ")} } from "@bondery/translations/generated/i18n/catalog-prefixes";`;
}

function generateClientHookFunction(ns) {
  const useName = useHookName(ns);
  const prefixType = prefixTypeName(ns);
  const hasPrefixes = (branchPrefixesByNamespace[ns] ?? []).length > 0;
  const lines = [
    `export function ${useName}(): import("@bondery/translations").TranslateFn<${JSON.stringify(ns)}>;`,
  ];

  if (hasPrefixes) {
    lines.push(
      `export function ${useName}<P extends ${prefixType}>(keyPrefix: P): import("@bondery/translations").TranslateFn<${JSON.stringify(ns)}, P>;`,
      `export function ${useName}(keyPrefix?: ${prefixType}) {`,
      `  return createNamespaceHook(${JSON.stringify(ns)}, keyPrefix);`,
      `}`,
    );
  } else {
    lines.push(
      `export function ${useName}() {`,
      `  return createNamespaceHook(${JSON.stringify(ns)});`,
      `}`,
    );
  }

  return lines.join("\n");
}

function generateServerHookFunction(ns) {
  const getName = getHookName(ns);
  const prefixType = prefixTypeName(ns);
  const hasPrefixes = (branchPrefixesByNamespace[ns] ?? []).length > 0;
  const lines = [
    `export function ${getName}(): Promise<import("@bondery/translations").TranslateFn<${JSON.stringify(ns)}>>;`,
  ];

  if (hasPrefixes) {
    lines.push(
      `export function ${getName}<P extends ${prefixType}>(keyPrefix: P): Promise<import("@bondery/translations").TranslateFn<${JSON.stringify(ns)}, P>>;`,
      `export async function ${getName}(keyPrefix?: ${prefixType}) {`,
      `  return createServerNamespaceHook(${JSON.stringify(ns)}, keyPrefix);`,
      `}`,
    );
  } else {
    lines.push(
      `export async function ${getName}() {`,
      `  return createServerNamespaceHook(${JSON.stringify(ns)});`,
      `}`,
    );
  }

  return lines.join("\n");
}

function generateClientHooks(platform, importFactory, platformLabel) {
  const namespaces = namespacesForPlatform(platform);
  const constName = `${platformLabel.toUpperCase()}_NAMESPACES`;
  const typeName = `${platformLabel}Namespace`;
  const lines = [
    "/** Generated by packages/translations/scripts/generate-client-i18n-hooks.mjs — do not edit. */",
    "",
    '"use client";',
    "",
    importFactory,
    prefixImportsForNamespaces(namespaces),
    "",
  ];

  for (const ns of namespaces) {
    lines.push(generateClientHookFunction(ns));
    lines.push("");
  }

  lines.push(`export const ${constName} = ${JSON.stringify(namespaces)} as const;`);
  lines.push(`export type ${typeName} = (typeof ${constName})[number];`);
  lines.push("");

  return lines.join("\n");
}

function generateServerHooks(platform) {
  const namespaces = namespacesForPlatform(platform);
  const lines = [
    "/** Generated by packages/translations/scripts/generate-client-i18n-hooks.mjs — do not edit. */",
    "",
    'import { createServerNamespaceHook } from "../createServerNamespaceHook";',
    prefixImportsForNamespaces(namespaces),
    "",
  ];

  for (const ns of namespaces) {
    lines.push(generateServerHookFunction(ns));
    lines.push("");
  }

  return lines.join("\n");
}

function generateNamespacesUnion() {
  const byPlatform = {};
  for (const platform of PLATFORMS) {
    byPlatform[platform] = namespacesForPlatform(platform);
  }

  const lines = [
    "/** Generated by packages/translations/scripts/generate-client-i18n-hooks.mjs — do not edit. */",
    "",
    `export const namespacesByPlatform = ${JSON.stringify(byPlatform, null, 2)} as const;`,
    "",
    "export type WebNamespace = (typeof namespacesByPlatform.web)[number];",
    "export type MobileNamespace = (typeof namespacesByPlatform.mobile)[number];",
    "export type ExtensionNamespace = (typeof namespacesByPlatform.extension)[number];",
    "export type Platform = keyof typeof namespacesByPlatform;",
    "",
  ];

  return lines.join("\n");
}

// Package-level namespace registry
const generatedI18nDir = join(packageRoot, "../src/generated/i18n");
mkdirSync(generatedI18nDir, { recursive: true });
writeFileSync(join(generatedI18nDir, "namespaces.ts"), generateNamespacesUnion());

// Webapp
const webappGenDir = join(repoRoot, "apps/webapp/src/lib/i18n/generated");
mkdirSync(webappGenDir, { recursive: true });
writeFileSync(
  join(webappGenDir, "hooks.ts"),
  generateClientHooks(
    "web",
    'import { createNamespaceHook } from "../createNamespaceHook";',
    "Web",
  ),
);
writeFileSync(join(webappGenDir, "hooks.server.ts"), generateServerHooks("web"));

// Mobile
const mobileGenDir = join(repoRoot, "apps/mobile/src/lib/i18n/generated");
mkdirSync(mobileGenDir, { recursive: true });
writeFileSync(
  join(mobileGenDir, "hooks.ts"),
  generateClientHooks(
    "mobile",
    'import { createNamespaceHook } from "../createNamespaceHook";',
    "Mobile",
  ),
);

// Chrome extension
const extGenDir = join(repoRoot, "apps/chrome-extension/src/lib/i18n/generated");
mkdirSync(extGenDir, { recursive: true });
writeFileSync(
  join(extGenDir, "hooks.ts"),
  generateClientHooks(
    "extension",
    'import { createNamespaceHook } from "../createNamespaceHook";',
    "Extension",
  ),
);

console.log("generate-client-i18n-hooks: ok");
