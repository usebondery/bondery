// Scans docs markdown frontmatter and generates the in-app doc link registry.
import { readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, relative, sep } from "node:path";
import { fileURLToPath } from "node:url";
import { parse as parseYaml } from "yaml";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, "..");
const DOCS_DIR = join(REPO_ROOT, "docs");
const OUTPUT_FILE = join(REPO_ROOT, "packages/helpers/src/docs/doc-links.generated.ts");

function walkMarkdownFiles(dir, acc = []) {
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) {
      walkMarkdownFiles(full, acc);
    } else if (entry.endsWith(".md")) {
      acc.push(full);
    }
  }
  return acc;
}

function parseFrontmatter(content) {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) return null;
  try {
    return parseYaml(match[1]);
  } catch {
    return null;
  }
}

function defaultDocPath(filePath) {
  const rel = relative(DOCS_DIR, filePath).split(sep).join("/");
  if (rel === "README.md") return "";
  return rel.replace(/\.md$/, "");
}

function main() {
  const entries = new Map();
  const duplicates = [];

  for (const filePath of walkMarkdownFiles(DOCS_DIR)) {
    const content = readFileSync(filePath, "utf8");
    const frontmatter = parseFrontmatter(content);
    if (!frontmatter?.docId) continue;

    const docPath = frontmatter.docPath ?? defaultDocPath(filePath);
    const pageId = frontmatter.docId;

    if (entries.has(pageId)) {
      duplicates.push(pageId);
    } else {
      entries.set(pageId, { path: docPath, sourceFile: filePath });
    }

    const sections = frontmatter.docSections;
    if (sections && typeof sections === "object") {
      for (const [sectionKey, anchor] of Object.entries(sections)) {
        const sectionId = `${pageId}.${sectionKey}`;
        if (entries.has(sectionId)) {
          duplicates.push(sectionId);
        } else {
          entries.set(sectionId, {
            path: docPath,
            hash: String(anchor),
            sourceFile: filePath,
          });
        }
      }
    }
  }

  if (duplicates.length > 0) {
    console.error("Duplicate docId values:", [...new Set(duplicates)].join(", "));
    process.exit(1);
  }

  const sortedIds = [...entries.keys()].sort();
  const lines = sortedIds.map((id) => {
    const entry = entries.get(id);
    const pathPart = `path: ${JSON.stringify(entry.path)}`;
    const hashPart = entry.hash ? `, hash: ${JSON.stringify(entry.hash)}` : "";
    return `  ${JSON.stringify(id)}: { ${pathPart}${hashPart} },`;
  });

  const output = `// Generated by scripts/generate-doc-links.mjs — do not edit.
export const DOC_LINKS = {
${lines.join("\n")}
} as const;

export type DocId = keyof typeof DOC_LINKS;
`;

  writeFileSync(OUTPUT_FILE, output, "utf8");
  console.log(`Wrote ${sortedIds.length} doc link(s) to ${relative(REPO_ROOT, OUTPUT_FILE)}`);
}

main();
